template <typename T>
bool sameReal(T a,T b){
  if (std::abs(a - b) < 1e-10) {
    return true;
  }
  return false;
}
/***************************************************************************************/ 
template <typename T>
void swapValues(T& a,T& b){
  T c = a;
  a = b;
  b = c;
}
